import json
from argparse import ArgumentParser, ArgumentDefaultsHelpFormatter, Namespace
from pathlib import Path
from warnings import warn
from datetime import datetime

import torch
import datasets
from transformers import AutoTokenizer, AutoModelForCausalLM
from peft import AutoPeftModelForCausalLM

from partages_llm.utils import basic_logger_init
from partages_llm.processing import get_mcq_answer_pattern
from partages_llm.eval.mcqa import mcqa

DESC="Evaluates a text generation model on one of the MCQ tasks from PARCOMED (frenchmedmcqa or mediqal)"
DATADIR_HELP="Where to find the dataset, as created by `preprocess/instruction_processing_pipeline_*"
NDOCS_HELP="Place a limit on the number of questions on which to evaluate; will take a random sample"
MAXGENTOKENS_HELP="Place a limit on the number of tokens to be generated by the model for each response"
CT_HELP="Path to a .jinja file containing the template to apply to the prompts; "\
"mandatory when the tokenizer associated with `model_path` doesn't already have a chat template"
TEMP_HELP="Modulation parameter for sampling from the logits distribution. Max value 1.0 gives maximal randomness"
TOPP_HELP="Percentage threshold on the tokens considered for inclusion in the generated text; "\
"see https://en.wikipedia.org/wiki/Top-p_sampling. Not valid for some models"
WRITE_HELP="Records results in a subdirectory of `data_dir`"
WRITEALL_HELP="Includes all generated text with the results. Only active alongside `-o` flag, "\
"i.e. pass `-o` for results only and `-oa` for full details"
DBMODE_HELP="Activate interactive response inspection in the evaluation function; read "\
"and validate answers as they are generated"
PEFT_HELP="Indicates that the model should be loaded with the peft library's constructor rather than "\
"the transformers one"


def parse_arguments() -> Namespace:
    dataset_names = "frenchmedmcqa", "mediqal"
    parser = ArgumentParser(description=DESC, formatter_class=ArgumentDefaultsHelpFormatter)
    parser.add_argument("model_path")
    parser.add_argument("data_dir", help=DATADIR_HELP)
    parser.add_argument("-v", "--dataset-version", type=int, default=0)
    parser.add_argument("-d", "--dataset-name", choices=dataset_names, default=dataset_names[0])
    parser.add_argument("--ndocs", type=int, help=NDOCS_HELP)
    parser.add_argument("--max-gen-tokens", type=int, default=24, help=MAXGENTOKENS_HELP)
    parser.add_argument("--batch-size", type=int, default=64)
    parser.add_argument("--pad-token")
    parser.add_argument("--ct", dest="chat_template", help=CT_HELP)
    parser.add_argument("-t", dest="temperature", type=float, default=0., help=TEMP_HELP)
    parser.add_argument("-p", dest="sampling_top_p", type=float, default=.05, help=TOPP_HELP)
    parser.add_argument("-o", dest="write_out", action="store_true", help=WRITE_HELP)
    parser.add_argument("-a", dest="write_out_all", action="store_true", help=WRITEALL_HELP)
    parser.add_argument("--db", dest="debug_mode", action="store_true", help=DBMODE_HELP)
    parser.add_argument("--peft", action="store_true", help=PEFT_HELP)
    return parser.parse_args()


def main():

    ## VARS SETUP ##
    logger = basic_logger_init()
    args = parse_arguments()

    ## INPUT DATA ##
    version_prefix = "v" + str(args.dataset_version)
    data_dir_base = Path(args.data_dir)
    formatting_run = list(data_dir_base.glob(version_prefix + "*/")).pop()
    data_path = formatting_run / args.dataset_name
    assert data_path.exists(), f"Tried to find {data_path} and couldn't"
    logger.info("Loading data from %s", data_path)
    if (data_path / "dataset_dict.json").exists():
        # train/eval split - we don't need the train data
        data_path = data_path / "validation"
    eval_dataset_text = datasets.load_from_disk(data_path)
    if args.ndocs:
        eval_dataset_text = eval_dataset_text.shuffle(seed=0).take(args.ndocs)
    
    ## MODEL SETUP ##
    logger.info("Loading model")
    device = "cuda" if torch.cuda.is_available() else "cpu"
    if args.peft:
        model = AutoPeftModelForCausalLM.from_pretrained(
            args.model_path,
            low_cpu_mem_usage=True,
            ignore_mismatched_sizes=True,
        )
    else:
        model = AutoModelForCausalLM.from_pretrained(
            args.model_path,
            dtype=torch.bfloat16
        )
    model = model.to(device)
    logger.info("Loaded to device `%s`: %s", device, type(model).__name__)

    ## PREPROCESSING ##
    tokenizer = AutoTokenizer.from_pretrained(args.model_path, padding_side="left")
    if tokenizer.chat_template is None:
        if args.chat_template is not None:
            with open(args.chat_template) as f:
                setattr(tokenizer, "chat_template", f.read())
        else:
            ct_path_maybe = Path(args.model_path) / "chat_template.jinja"
            if ct_path_maybe.exists():
                with ct_path_maybe.open() as f:
                    setattr(tokenizer, "chat_template", f.read())
            else:
                warn("No chat template found: this may break tokenisation", RuntimeWarning)
    if not tokenizer.pad_token:
        tokenizer.add_special_tokens({
            "pad_token": tokenizer.eos_token if args.pad_token is None else args.pad_token
        })
    logger.info("Processing prompts")
    eval_dataset_templated = eval_dataset_text.map(
        lambda x: {
            "templated_prompt": tokenizer.apply_chat_template(
                x["prompt"],
                tokenize=False,
                add_generation_prompt=True,
                enable_thinking=False
            )
        },
        desc="Applying chat template"
    )
    eval_dataset_tokenized_input = eval_dataset_templated.map(
        lambda x: tokenizer(x["templated_prompt"]),
        remove_columns=["prompt"],
        desc="Tokenizing"
    )
    sorted_dataset = eval_dataset_tokenized_input.map(
        lambda x: {"seq_len": len(x["input_ids"])},
        desc="Counting tokens",
    ).sort("seq_len", reverse=False)
    logger.info("Max. sequence length %d", max(sorted_dataset["seq_len"]))

    ## EVALUATION ##
    mcq_answer_pattern = get_mcq_answer_pattern(eval_dataset_text)
    return_all_outputs = args.write_out and args.write_out_all
    logger.info("Launching generations")
    result = mcqa(
        model,
        tokenizer,
        dataset=sorted_dataset,
        top_p=args.sampling_top_p,
        batch_size=args.batch_size,
        temperature=args.temperature,
        max_new_tokens=args.max_gen_tokens,
        return_all_outputs=return_all_outputs,
        inspect_responses_live=args.debug_mode,
        mcq_answer_pattern=mcq_answer_pattern
    )
    t = "\n\t\t\t\t - "
    metric_disp_str = t.join(
        f"{k.upper()} = {round(v * 100, 4)}" for k, v in result["metrics"].items()
    )
    logger.info("EVAL SET METRICS:%s%s", t, metric_disp_str)

    ## WRAP UP ##
    if args.write_out:
        results_dir_path = data_dir_base / "eval-results/icl"
        timestamp = datetime.now().strftime("%y-%m-%d_%H-%M")
        output_dirname = "-".join((Path(args.model_path).name, args.dataset_name, timestamp))
        output_path = results_dir_path / output_dirname
        output_path.mkdir(parents=True)
        arg_dict = vars(args)
        arg_dict["input_dataset_full_path"] = str(data_path)
        with (output_path / "script_params.json").open("w") as f:
            json.dump(arg_dict, f, indent=4)
        with (output_path / "results.json").open("w") as f:
            json.dump(result, f, indent=4)
        logger.info("Results saved @ %s", output_path)
    print("=" * 75)


if __name__ == "__main__":
    main()

